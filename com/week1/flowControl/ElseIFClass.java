package com.week1.flowControl;

public class ElseIFClass {
	
	public static void main(String[] args) {
		int time = 14;
		
		if(time <10) {
			System.out.println("Good Morning!");
		}
		else if(time < 18 ) {
			System.out.println("Good Afternoon:)");
		}
		else {
			System.out.println("Good Evening!");
		}
	}

}
